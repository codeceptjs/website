import fs from 'node:fs';
import path from 'node:path';
import { spawnSync } from 'node:child_process';

const REPO_URL = process.env.CODECEPTJS_DOCS_REPO || 'https://github.com/codeceptjs/CodeceptJS.git';
const BRANCH = process.env.CODECEPTJS_DOCS_BRANCH || '4.x';
const SYNC_ROOT = path.join(process.cwd(), '.codeceptjs-docs');
const REPO_DIR = path.join(SYNC_ROOT, 'repo');
const STAGING_DIR = path.join(SYNC_ROOT, 'staging');
const OUTPUT_DIR = path.join(process.cwd(), 'src', 'content', 'docs');
const ASTRO_CONFIG = path.join(process.cwd(), 'astro.config.mjs');
const MARKER = '<!-- Auto-generated by scripts/sync-codeceptjs-docs.mjs from codeceptjs/CodeceptJS@4.x. Do not edit. -->';

// Sidebar links the script must NOT copy (managed elsewhere or local-only).
const SKIP_LINKS = new Set([
  'changelog',           // managed by scripts/sync-changelog.mjs
  'web-api',             // generated by scripts/generate-unified-api.mjs
  'mobile-api',          // generated by scripts/generate-unified-api.mjs
  'cheatsheet',          // local .mdx
]);

const checkMode = process.argv.includes('--check');

function toKebab(name) {
  return name
    .replace(/([A-Z]+)([A-Z][a-z])/g, '$1-$2')
    .replace(/([a-z\d])([A-Z])/g, '$1-$2')
    .toLowerCase();
}

function normalizeRel(rel) {
  const segments = rel.split('/');
  const file = segments.pop();
  const dot = file.lastIndexOf('.');
  const stem = dot >= 0 ? file.slice(0, dot) : file;
  const ext = dot >= 0 ? file.slice(dot) : '';
  return [...segments.map(toKebab), toKebab(stem) + ext.toLowerCase()].join('/');
}

function shallowClone() {
  fs.rmSync(REPO_DIR, { recursive: true, force: true });
  fs.mkdirSync(SYNC_ROOT, { recursive: true });
  const result = spawnSync(
    'git',
    ['clone', '--depth=1', '--branch', BRANCH, '--single-branch', REPO_URL, REPO_DIR],
    { stdio: 'inherit' }
  );
  if (result.error && result.error.code === 'ENOENT') {
    throw new Error('git executable not found on PATH; install git to run this sync.');
  }
  if (result.status !== 0) {
    throw new Error(`git clone failed (exit ${result.status}) for ${REPO_URL}@${BRANCH}`);
  }
}

function walk(dir, relBase = '') {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  const files = [];
  for (const entry of entries) {
    const rel = relBase ? path.posix.join(relBase, entry.name) : entry.name;
    const abs = path.join(dir, entry.name);
    if (entry.isDirectory()) {
      files.push(...walk(abs, rel));
    } else if (entry.isFile()) {
      files.push({ abs, rel });
    }
  }
  return files;
}

function titleFromFilename(relPath) {
  const base = path.basename(relPath, path.extname(relPath));
  return base.replace(/[-_]+/g, ' ').replace(/\b\w/g, (c) => c.toUpperCase());
}

function expectedSlug(relPath) {
  return relPath.replace(/\.md$/i, '').toLowerCase();
}

function parseFrontmatter(text) {
  const normalized = text.replace(/\r\n/g, '\n');
  const match = normalized.match(/^---\n([\s\S]*?)\n---\n?/);
  if (!match) return { fields: {}, body: normalized };
  const block = match[1];
  const body = normalized.slice(match[0].length);
  const fields = {};
  for (const line of block.split('\n')) {
    const m = line.match(/^([A-Za-z0-9_-]+)\s*:\s*(.*)$/);
    if (!m) continue;
    fields[m[1]] = m[2].trim();
  }
  return { fields, body };
}

function stripLeadingH1(body) {
  // Starlight renders its own H1 from `title:`. Drop a leading `# Heading` so
  // the page doesn't show two H1s.
  return body.replace(/^#\s+[^\n]*\n+/, '');
}

function transformMarkdown(srcText, outRel) {
  const { fields, body } = parseFrontmatter(srcText);
  const title = (fields.title && fields.title.length > 0) ? fields.title : titleFromFilename(outRel);
  const out = ['---', `title: ${title}`];
  if (fields.permalink) {
    const normalizedPermalink = fields.permalink.replace(/^\//, '').toLowerCase();
    if (normalizedPermalink && normalizedPermalink !== expectedSlug(outRel)) {
      out.push(`slug: ${normalizedPermalink}`);
    }
  }
  out.push('---', MARKER, '');
  return out.join('\n') + stripLeadingH1(body.replace(/^\n+/, ''));
}

function buildStaging() {
  fs.rmSync(STAGING_DIR, { recursive: true, force: true });
  const docsDir = path.join(REPO_DIR, 'docs');
  if (!fs.existsSync(docsDir)) {
    throw new Error(`No docs/ directory found in cloned repo at ${docsDir}`);
  }
  const files = walk(docsDir);
  for (const { abs, rel } of files) {
    const outRel = normalizeRel(rel);
    const target = path.join(STAGING_DIR, outRel);
    fs.mkdirSync(path.dirname(target), { recursive: true });
    if (outRel.toLowerCase().endsWith('.md')) {
      fs.writeFileSync(target, transformMarkdown(fs.readFileSync(abs, 'utf8'), outRel));
    } else {
      fs.copyFileSync(abs, target);
    }
  }
  return files.length;
}

function collectSidebarLinks() {
  const config = fs.readFileSync(ASTRO_CONFIG, 'utf8');
  const links = new Set();
  for (const m of config.matchAll(/\blink:\s*['"]([^'"]+)['"]/g)) {
    links.add(m[1].replace(/^\/+|\/+$/g, ''));
  }
  const topicsMatch = config.match(/topics\s*:\s*{([\s\S]*?)\n\s*}/);
  if (topicsMatch) {
    for (const m of topicsMatch[1].matchAll(/['"]([^'"]+)['"]/g)) {
      links.add(m[1].replace(/^\/+|\/+$/g, ''));
    }
  }
  return links;
}

function stripStagedSlug(text) {
  // The destination file path determines the URL, so a carried-over upstream
  // `slug:` (from `permalink:`) would only force a divergent route.
  return text.replace(/^slug:\s*.+\n/m, '');
}

function copyCurated(links) {
  let written = 0;
  let unchanged = 0;
  const missing = [];
  const drift = [];
  for (const link of links) {
    if (SKIP_LINKS.has(link)) continue;
    const srcFile = path.join(STAGING_DIR, `${link}.md`);
    if (!fs.existsSync(srcFile)) {
      missing.push(link);
      continue;
    }
    const target = path.join(OUTPUT_DIR, `${link}.md`);
    const newContent = Buffer.from(stripStagedSlug(fs.readFileSync(srcFile, 'utf8')), 'utf8');
    let existing = null;
    try { existing = fs.readFileSync(target); } catch (e) {
      if (e.code !== 'ENOENT') throw e;
    }
    if (existing && existing.equals(newContent)) {
      unchanged += 1;
      continue;
    }
    if (!checkMode) {
      fs.mkdirSync(path.dirname(target), { recursive: true });
      fs.writeFileSync(target, newContent);
    }
    written += 1;
    drift.push(`${link}.md`);
  }
  return { written, unchanged, missing, drift };
}

async function main() {
  console.log(`Cloning ${REPO_URL}@${BRANCH} into ${REPO_DIR}…`);
  shallowClone();
  const stagedCount = buildStaging();
  console.log(`staged ${stagedCount} files in ${STAGING_DIR}`);

  const links = collectSidebarLinks();
  const result = copyCurated(links);
  const considered = links.size - [...SKIP_LINKS].filter((l) => links.has(l)).length;

  if (checkMode) {
    if (result.drift.length > 0) {
      console.error(`sync:docs:check failed — ${result.drift.length} files would change:`);
      for (const f of result.drift.slice(0, 20)) console.error(`  ${f}`);
      if (result.drift.length > 20) console.error(`  …and ${result.drift.length - 20} more`);
      process.exit(1);
    }
    console.log(`sync:docs:check ok — ${considered} curated docs in sync`);
    return;
  }

  console.log(`copied ${result.written} (${result.unchanged} unchanged) of ${considered} sidebar-listed docs`);
  if (result.missing.length > 0) {
    console.log(`note: ${result.missing.length} sidebar links have no upstream source (kept local-only or generated):`);
    for (const m of result.missing.slice(0, 30)) console.log(`  ${m}`);
    if (result.missing.length > 30) console.log(`  …and ${result.missing.length - 30} more`);
  }
}

main().catch((err) => {
  console.error(err.message);
  process.exit(1);
});
